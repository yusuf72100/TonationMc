                        <small class="text-white mb-4">
                          Le serveur est orienté vers le futur et plus précisément en 2050. Le régime soviétique russe n'est jamais tombé et pire, il a réalisé une expansion notable. En effet les russes ont envahis tous les pays entre la Russie et la France et ont même gagner une guerre contre les USA, guerre qui marquera la fin des états-unis. Dans cette terrible guerre la France s'en sortira indemne non pas parce qu'elle a gagné la guerre mais parce qu'elle est resté neutre tout au long de la guerre. Aujourd'hui, en 2050, la France et la Russie sont 2 pays voisins, neutres et avec des relations diplomatiques.
