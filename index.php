                        <h4 class="text-white mb-4">Résultat de la guerre</h4>
                        <small class="text-white mb-4">
                          Il faut noter qu'après cette guerre, deux groupes armés ont faits leur apparition. Ces deux groupes sont tout les deux issues de la souche russe mais ils auront des intérêts totalement différents.

D'un côté nous avons les rebelles qui sont des résistants de début de guerre et qui se sont opposés tout au long de la guerre au régime communiste et qui s'oppsent encore dans le but de faire tomber le régime sans pour autant occasionner des dégâts sur l'économie du pays.

De l'autre nous avons les terroristes. Ces derniers sont pour la plupart, des anciens soldats de la guerre qui ont mis en place un système ayant pour but de continuer l'expansion de la Russie. Autrement dit, ce sont des russes radicales qui ont pour unique but de détruire la France et de se l'approprié.
